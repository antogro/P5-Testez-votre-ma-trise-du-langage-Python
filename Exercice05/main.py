def sum(a, b):
    """la fonction sum prend en charge 2 parametres,
        et retourne l'addition a et b
    Args:
        a (int): chiffre ou nombre à additionner
        b (int): chiffre ou nombre à additionner

    Returns:
        int: a + b
    """
    return a + b


def subtraction(a, b):
    """la fonction soustraction prend en charge 2 parametre
    et retourne la soustraction de a avec b
    Args:
        a (int): chiffre ou nombre à soustraire
        b (int): chiffre ou nombre à soustraire

    Returns:
        int: a - b
    """
    return a - b
